package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan = 3;
        switch (jalanJalan){
            case 1:
                System.out.println("cabe cabean");
                break;
            case 2:
                System.out.println("penjual tamu lawak sani");
                break;
            case 3:
                System.out.println("nyasaki jalan");
                break;
            case 4:
                System.out.println("buang buang bensin");
                break;
            default:
                System.out.println("nusia nganga");
                break;
        }
    }
}
